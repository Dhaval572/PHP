<?php

/*
	Associative arrays are arrays where each element is associated with a custom key (or index), rather than a numeric index.( like map in c++ language )
*/

// Associative Array
$student = array
(
    "name" => "John",
    "age" => 20,
    "course" => "PHP"
);

echo $student["name"]."<br>";  
echo $student["age"]."<br>";   
echo $student["course"]."<br>"; 
?>
